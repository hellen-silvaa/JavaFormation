package Fundamentos.exercicios;

public class ContagemDoces {
    static void main() {
        int contadorDoces = 1;
        while (contadorDoces <= 3){
            System.out.println("Número de doces: " + contadorDoces);
            contadorDoces++;
        }
        System.out.println("Não pode comer mais doces.");
    }
}
