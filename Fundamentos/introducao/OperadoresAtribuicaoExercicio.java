package Fundamentos.introducao;

public class OperadoresAtribuicaoExercicio {
    public static void main(String[] args) {
        Integer numero = 5;

        // Atribuição de adição (+= 3)
        numero += 3;  // numero agora é 8

        // Atribuição de subtração (-= 2)
        numero -= 2;  // numero agora é 6

        // Atribuição de multiplicação (*= 4)
        numero *= 4;  // numero agora é 24

        // Atribuição de divisão (/= 2)
        numero /= 2;  // numero agora é 12

        System.out.println("Valor final: " + numero); // Vai imprimir 12
    }
}
